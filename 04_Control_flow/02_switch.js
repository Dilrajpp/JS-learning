const month = 4

switch (month) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("fab");
        break;

    case 3:
        console.log("march");
        break;
    case 4:
        console.log("april");
        // break;
        break;
    case 5:
        console.log("may");
        break;

    default:
        console.log("default case match");
        
        break;
}




